# 변수 초기화
firstNum = 10
secondNum = 3
firstStr = 'Hello'
secondStr = ' '
thirdStr = 'python'

## 덧셈 연산
print(firstNum+secondNum)
print(firstStr+secondStr+thirdStr)
# 숫자와 문자열을 덧셈연산하면 에러가 발생한다.
# print(firstStr+secondNum+thirdStr)

## 뺄셈 연산
print(firstNum-secondNum)
# 문자열을 뺄셈 연산하면 에러가 발생한다. 문자열을 이용한 연산은 덧셈만 가능하다.
# print('ab'-'ef')

## 곱셈 연산
print(firstNum*secondNum)

## 나눗셈 연산
print(firstNum/secondNum)

## 나머지 연산
print(firstNum%secondNum)

## 몫 연산
print(firstNum//secondNum)


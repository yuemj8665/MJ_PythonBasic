## 비교 연산자
# 비교연산자의 열과는 boolean 타입으로 나온다(True/False)

# 숫자비교
firstNum = 10
secondNum = 3
# ==
print(firstNum==secondNum) # False
# !=
print(firstNum!=secondNum) # True
# >
print(firstNum>secondNum) # True
# >=
print(firstNum>=secondNum) # True
# <
print(firstNum<secondNum) # False
# <=
print(firstNum<=secondNum) # False

# 문자 비교 1
firstStr = 'Hello'
secondStr = 'python'
# ==
print(firstStr==secondStr)
# !=
print(firstStr!=secondStr)

# 문자 비교 2
firstChar = 'a'
secondChar = 'z'
# ==
print(firstChar==secondChar)
# !=
print(firstChar!=secondChar)
# >
print(firstChar>secondChar)
# >=
print(firstChar>=secondChar)
# <
print(firstChar<secondChar)
# <=
print(firstChar<=secondChar)
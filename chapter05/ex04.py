# 논리 연산자

# 참거짓 비교
firstBool = True
secondBool = False

# not
print(not firstBool) # True의 not

# and
print(firstBool and secondBool) # True and False = False
print(firstBool and not secondBool) # True and True = True

# or
print(firstBool or secondBool) # True or False = True
print(not firstBool or secondBool) # False or False = False